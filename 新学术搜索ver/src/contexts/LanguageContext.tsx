export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>('zh');

  const t = (key: string, params?: Record<string, string>): string => {
    const template = translations[language][key] || key;
    if (!params) {
      return template;
    }

    return Object.entries(params).reduce(
      (result, [paramKey, value]) => result.replaceAll(`{${paramKey}}`, value),
      template,
    );
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    // Fallback for hot reload scenarios where provider reference is lost
    const fallbackT = (key: string, params?: Record<string, string>): string => {
      const template = translations['zh'][key] || key;
      if (!params) {
        return template;
      }

      return Object.entries(params).reduce(
        (result, [paramKey, value]) => result.replaceAll(`{${paramKey}}`, value),
        template,
      );
    };
    return { language: 'zh' as Language, setLanguage: (() => {}) as (lang: Language) => void, t: fallbackT };
  }
  return context;
}
